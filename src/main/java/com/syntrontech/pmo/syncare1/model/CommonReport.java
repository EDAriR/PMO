package com.syntrontech.pmo.syncare1.model;

public class CommonReport {

	private String city;
	private String fieldNames;
	private String excelTemplatType;
	
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getFieldNames() {
		return fieldNames;
	}
	public void setFieldNames(String fields) {
		this.fieldNames = fields;
	}
	public String getExcelTemplatType() {
		return excelTemplatType;
	}
	public void setExcelTemplatType(String excelTemplatType) {
		this.excelTemplatType = excelTemplatType;
	}
}
